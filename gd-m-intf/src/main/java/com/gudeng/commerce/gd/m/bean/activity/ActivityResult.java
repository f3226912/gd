package com.gudeng.commerce.gd.m.bean.activity;

public class ActivityResult {

	/**
	 * 活动类型
	 */
	private int type ;

	/**
	 * 活动执行结果
	 */
	private Object result ;

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public Object getResult() {
		return result;
	}

	public void setResult(Object result) {
		this.result = result;
	}

}
